/**
 * Cookie-free admin client — uses the service role key without passing any
 * user JWT cookies. This guarantees true RLS bypass for tables that have RLS
 * enabled with no permissive policies (e.g. bank_share_otps, bank_share_access_log).
 * Use this for any server-side operation that must not be constrained by RLS.
 */
export function createAdminClient() {
  return createSupabaseClient<Database>(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!,
    {
      auth: {
        autoRefreshToken: false,
        persistSession: false,
      },
    }
  );
}
